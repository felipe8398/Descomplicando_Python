#! /usr/bin/env python3

"""
Os tipos primários também chamados de tipos "escalares" (scalar types) são utilizados para armazenar 
uma única unidade de informação como por exemplo um número ou um texto como vimos anteriormente.
"""

print("Imprimindo um inteiro")
print("int = 1")
print("print(int)")
int = 1
print(type(int))
print("")

print("Imprimindo um str")
print("str = \"felipe\"")
print("print(str)")
str = "felipe" 
print(type(str))
print("")


print("Imprimindo um float")
print("float = 2.5")
print("print(float)")
float = 2.5 
print(type(float))
print("")

print("Imprimindo um Bolean")
print("Bolean = True")
print("print(Bolean)")
Bolean = True
print(type(Bolean))
print("")


print("Imprimindo um NoneType")
print("NoneType = None")
print("print(NoneType)")
NoneType = None
print(type(NoneType))
print("")